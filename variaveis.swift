// declarando variáveis
var nome = "Francilene"
var idade = 28

var sobrenome = "Silva"
idade = 29

// criando constantes - o valor das constantes não pode ser alterado
let pi = 3.14
let cachorro = "Sparky"

// imprimindo na tela
print(idade)

// concatenação
print(nome + " " + sobrenome)

// interpolação
print ("Meu primeiro nome é \(nome), e meu último nome é \(sobrenome)")

var numero1 = 10;
var numero2 = 20

print("Meu primeiro numero é: \(numero1). O meu segundo número é: \(numero2)")
print("A soma dos dois números é  \(numero1 + numero2)")

/*
    Nomenclatura de constante e variáveis podem conter qualquer caractere,
    incluindo caracteres Unicode.
    Mas não pode conter caracteres de espaço em branco, simbolos matemáticos, 
    setas, valores escalares Unicode de uso privado ou caracteres de desenho de linha
    e caixa.
    Swift não exige ponto e vírgula, mas se desejar pode colocar. No entanto, são necessários se você
    quiser escrever várias instruções separadas em uma linha única
*/

// variáveis com qualquer caractere
let π = 3.14159
let 你好 = "你好世界"
let 🐶🐮 = "dogcow"

// várias instruções em uma linha
let cat = "🐱"; print(cat)
